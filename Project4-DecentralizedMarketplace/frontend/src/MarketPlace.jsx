const contractAddress = '0x2840d7CDC9BCE4F8e53D4b519562d751d9B65D04';

function Marketplace() {
    const [account, setAccount] = useState('');
    const [web3, setWeb3] = useState(null);
    const [contract, setContract] = useState(null);
    const [mintedItems, setMintedItems] = useState([]);
    const [listedItems, setListedItems] = useState([]);
    const [tokenURI, setTokenURI] = useState('');
    const [price, setPrice] = useState('');
    const [tokenId, setTokenId] = useState('');

    useEffect(() => {
        loadBlockchainData();
    }, []);

    async function loadBlockchainData() {
        if (window.ethereum) {
            const web3Instance = new Web3(window.ethereum);
            setWeb3(web3Instance);
            await window.ethereum.enable();

            const accounts = await web3Instance.eth.getAccounts();
            setAccount(accounts[0]);

            const marketplaceContract = new web3Instance.eth.Contract(contractABI, contractAddress);
            setContract(marketplaceContract);

            // Load minted and listed items
            const minted = await marketplaceContract.methods.getMintedItems(accounts[0]).call();
            const listed = await marketplaceContract.methods.getListedItems().call();

            setMintedItems(minted);
            setListedItems(listed);
        } else {
            alert('Please install MetaMask to use this DApp.');
        }
    }

    async function mintItem() {
        if (contract && account) {
            try {
                await contract.methods.mintItem(tokenURI).send({ from: account });
                setTokenURI('');
                loadBlockchainData(); // Reload data after minting
            } catch (error) {
                console.error('Minting failed:', error);
            }
        }
    }
    async function loadMintedItems(contract, account) {
        const minted = await contract.methods.getMintedItems(account).call();
        const mintedWithDetails = await Promise.all(minted.map(async (id) => {
            const uri = await contract.methods.tokenURI(id).call();
            return { id, uri };
        }));
        setMintedItems(mintedWithDetails);
    }

    async function loadListedItems(contract) {
        const listed = await contract.methods.getListedItems().call();
        const listedWithDetails = await Promise.all(listed.map(async (id) => {
            const price = await contract.methods.itemPrices(id).call();
            const uri = await contract.methods.tokenURI(id).call();
            return { id, price: web3.utils.fromWei(price, 'ether'), uri };
        }));
        setListedItems(listedWithDetails);
    }
    async function listItem(tokenId, price) {
        if (contract && account) {
            const weiPrice = web3.utils.toWei(price, 'ether');
            try {
                await contract.methods.listItem(tokenId, weiPrice).send({ from: account });
                loadBlockchainData(); // Reload data after listing
            } catch (error) {
                console.error('Listing failed:', error);
            }
        }
    }

    return (
        <Container>
            <Typography variant="h4" gutterBottom>Decentralized Marketplace</Typography>
            <Typography variant="h6">Account: {account}</Typography>

            {/* Form to mint a new NFT */}
            <Grid container spacing={2} style={{ marginTop: '20px' }}>
                <Grid item xs={12} sm={6}>
                    <Typography variant="h6">Mint New Digital Art</Typography>
                    <TextField
                        fullWidth
                        label="Token URI"
                        variant="outlined"
                        value={tokenURI}
                        onChange={e => setTokenURI(e.target.value)}
                        style={{ marginBottom: '10px' }}
                    />
                    <Button variant="contained" color="primary" onClick={mintItem}>
                        Mint Item
                    </Button>
                </Grid>
            </Grid>

            {/* Minted Items */}
            <Typography variant="h6" style={{ marginTop: '20px' }}>Minted Items</Typography>
            <Grid container spacing={2}>
                {mintedItems.length === 0 ? (
                    <Typography>No items minted yet.</Typography>
                ) : (
                    mintedItems.map(itemId => (
                        <Grid item xs={12} sm={6} md={4} key={itemId}>
                            <Card>
                                <CardContent>
                                    <Typography variant="subtitle1"><strong>Token ID:</strong> {itemId}</Typography>
                                    <Typography variant="subtitle1"><strong>Token URI:</strong> {tokenURI}</Typography>
                                </CardContent>
                                <CardActions>
                                    <TextField
                                        fullWidth
                                        label="Price in ETH"
                                        variant="outlined"
                                        onChange={e => setPrice(e.target.value)}
                                        style={{ marginRight: '10px' }}
                                    />
                                    <Button variant="contained" color="secondary" onClick={() => listItem(itemId, price)}>
                                        List Item
                                    </Button>
                                </CardActions>
                            </Card>
                        </Grid>
                    ))
                )}
            </Grid>

            {/* Listed Items */}
            <Typography variant="h6" style={{ marginTop: '20px' }}>Listed Items</Typography>
            <Grid container spacing={2}>
                {listedItems.length === 0 ? (
                    <Typography>No items listed for sale yet.</Typography>
                ) : (
                    listedItems.map(itemId => (
                        <Grid item xs={12} sm={6} md={4} key={itemId}>
                            <Card>
                                <CardContent>
                                    <Typography variant="subtitle1"><strong>Token ID:</strong> {itemId}</Typography>
                                    <Typography variant="subtitle1"><strong>Price:</strong> {price} ETH</Typography>
                                </CardContent>
                                <CardActions>
                                    <Button variant="contained" color="primary">
                                        Buy Item
                                    </Button>
                                </CardActions>
                            </Card>
                        </Grid>
                    ))
                )}
            </Grid>
        </Container>
    );
}

export default Marketplace;
